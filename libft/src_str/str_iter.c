#include "../include/libft.h"

void	ft_str_check_iter(char **s, bool (*f)(char))
{
	if (!s || !*s || !f)
		return ;
	while (**s)
	{
		if (!f(**s))
			break ;
		(*s)++;
	}
	return ;
}
